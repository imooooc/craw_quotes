from scrapy.exceptions import DropItem

class TextPipeline(object):
    '''如果超过50字就省略'''
    def __init__(self):
        self.limit = 50

    def process_item(self, item, spider):
        if item['text']:
            if len(item['text']) > self.limit:
                item['text'] = item['text'][0:50] + '...'
            return item
        else:
            return DropItem('Missing Text')